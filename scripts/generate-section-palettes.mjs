import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(SCRIPT), '..');
const DATA_PATH = path.join(ROOT, 'data/section-palettes.json');
const OUTPUT_PATH = path.join(ROOT, 'css/section-palettes.css');
const HEX = /^#[0-9a-f]{6}$/i;

function rgb(hex) {
  return [1, 3, 5].map((start) => Number.parseInt(hex.slice(start, start + 2), 16)).join(', ');
}

function validate(data) {
  if (!data || data.version !== 1 || !data.sections || typeof data.sections !== 'object') {
    throw new Error('section-palettes.json must use schema version 1');
  }
  for (const [section, definition] of Object.entries(data.sections)) {
    if (!/^[a-z]+$/.test(section) || !definition.page || !definition.label || !definition.rationale) {
      throw new Error(`invalid section metadata: ${section}`);
    }
    for (const theme of ['light', 'dark']) {
      for (const token of ['accent', 'accentAlt', 'onAccent']) {
        if (!HEX.test(definition[theme]?.[token] || '')) {
          throw new Error(`invalid ${section}.${theme}.${token}`);
        }
      }
    }
  }
}

export function renderSectionPalettes(data) {
  validate(data);
  const lines = [
    '/* Generated by scripts/generate-section-palettes.mjs. Do not edit by hand. */',
    ':root {',
    '  --section-accent: #153b33;',
    '  --section-accent-alt: #24594b;',
    '  --section-accent-rgb: 21, 59, 51;',
    '  --section-on-accent: #f8f3e7;',
    '}',
    ''
  ];

  for (const theme of ['light', 'dark']) {
    for (const [section, definition] of Object.entries(data.sections)) {
      const palette = definition[theme];
      lines.push(
        `html[data-theme="${theme}"] body[data-section="${section}"] {`,
        `  --section-accent: ${palette.accent};`,
        `  --section-accent-alt: ${palette.accentAlt};`,
        `  --section-accent-rgb: ${rgb(palette.accent)};`,
        `  --section-on-accent: ${palette.onAccent};`,
        '}',
        ''
      );
    }
  }

  const uniform = data.sections.home;
  for (const theme of ['light', 'dark']) {
    const palette = uniform[theme];
    lines.push(
      `html[data-study-condition="a"][data-theme="${theme}"] body[data-section] {`,
      `  --section-accent: ${palette.accent};`,
      `  --section-accent-alt: ${palette.accentAlt};`,
      `  --section-accent-rgb: ${rgb(palette.accent)};`,
      `  --section-on-accent: ${palette.onAccent};`,
      '}',
      ''
    );
  }

  lines.push(
    'body[data-section] {',
    '  --section-border: rgba(var(--section-accent-rgb), 0.22);',
    '  --section-surface: linear-gradient(135deg, rgba(var(--section-accent-rgb), 0.035), rgba(var(--section-accent-rgb), 0.075)), linear-gradient(135deg, rgba(250, 248, 240, 0.985), rgba(230, 239, 231, 0.96));',
    '  --photo-overlay-inner: linear-gradient(180deg, rgba(var(--section-accent-rgb), 0.08) 0%, rgba(235, 241, 233, 0.76) 50%, rgba(245, 241, 231, 0.97) 100%);',
    '}',
    '',
    'body[data-section="home"] {',
    '  --photo-overlay-home: linear-gradient(180deg, rgba(var(--section-accent-rgb), 0.10) 0%, rgba(232, 239, 231, 0.62) 52%, rgba(245, 241, 231, 0.97) 100%);',
    '}',
    '',
    'html[data-theme="dark"] body[data-section] {',
    '  --section-border: rgba(var(--section-accent-rgb), 0.24);',
    '  --section-surface: linear-gradient(135deg, rgba(var(--section-accent-rgb), 0.035), rgba(var(--section-accent-rgb), 0.075)), linear-gradient(135deg, rgba(13, 45, 37, 0.98), rgba(8, 32, 26, 0.94));',
    '  --photo-overlay-inner: linear-gradient(180deg, rgba(var(--section-accent-rgb), 0.08) 0%, rgba(10, 45, 37, 0.76) 50%, rgba(7, 27, 22, 0.95) 100%);',
    '}',
    '',
    'html[data-theme="dark"] body[data-section="home"] {',
    '  --photo-overlay-home: linear-gradient(180deg, rgba(var(--section-accent-rgb), 0.08) 0%, rgba(11, 48, 39, 0.65) 52%, rgba(7, 27, 22, 0.95) 100%);',
    '}',
    '',
    'body[data-section] .header::after,',
    'body[data-section] .section-title::before,',
    'body[data-section] .kicker::before {',
    '  background: var(--section-accent);',
    '}',
    '',
    'body[data-section] .header,',
    'body[data-section] .section-title,',
    'body[data-section] .card,',
    'body[data-section] .quick-access__item,',
    'body[data-section] .ext-link-row,',
    'body[data-section] .rest-card,',
    'body[data-section] .forecast-widget,',
    'body[data-section] .forecast-day,',
    'body[data-section] .accordion details,',
    'body[data-section] .filter-bar--multi,',
    'body[data-section] .rest-filter-bar,',
    'body[data-section] .modulo-toc {',
    '  border-color: var(--section-border);',
    '}',
    '',
    'html[data-theme] body[data-section] .header,',
    'html[data-theme] body[data-section] .section-title,',
    'html[data-theme] body[data-section] .card,',
    'html[data-theme] body[data-section] .quick-access__item,',
    'html[data-theme] body[data-section] .ext-link-row,',
    'html[data-theme] body[data-section] .rest-card,',
    'html[data-theme] body[data-section] .forecast-widget,',
    'html[data-theme] body[data-section] .forecast-day,',
    'html[data-theme] body[data-section] .accordion details,',
    'html[data-theme] body[data-section] .filter-bar--multi,',
    'html[data-theme] body[data-section] .rest-filter-bar,',
    'html[data-theme] body[data-section] .modulo-toc {',
    '  background: var(--section-surface);',
    '}',
    '',
    'html[data-theme] body[data-section] .lang-selector button[aria-pressed="true"],',
    'html[data-theme] body[data-section] .theme-selector button[aria-pressed="true"],',
    'html[data-theme] body[data-section] .rest-filter__btn--active,',
    'html[data-theme] body[data-section] .tag[aria-pressed="true"] {',
    '  background: linear-gradient(145deg, var(--section-accent-alt), var(--section-accent));',
    '  border-color: var(--section-accent);',
    '  color: var(--section-on-accent);',
    '  box-shadow: 0 8px 20px rgba(var(--section-accent-rgb), 0.24), inset 0 1px 0 rgba(255, 255, 255, 0.18);',
    '}',
    '',
    'body[data-section] .accordion details[open],',
    'body[data-section] .ficha-details[open] {',
    '  border-color: var(--section-accent);',
    '}',
    '',
    '@media (hover: hover) {',
    '  body[data-section] a.card:hover,',
    '  body[data-section] a.ext-link-row:hover,',
    '  body[data-section] .quick-access__item:hover,',
    '  body[data-section] .rest-card:hover {',
    '    border-color: var(--section-accent);',
    '    box-shadow: 0 22px 52px rgba(var(--section-accent-rgb), 0.2), inset 0 1px 0 rgba(255, 255, 255, 0.62);',
    '  }',
    '}',
    '',
    'body[data-section] a:focus-visible,',
    'body[data-section] button:focus-visible,',
    'body[data-section] summary:focus-visible {',
    '  outline-color: var(--section-accent);',
    '}'
  );

  return `${lines.join('\n')}\n`;
}

if (process.argv[1] && path.resolve(process.argv[1]) === SCRIPT) {
  const data = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));
  fs.writeFileSync(OUTPUT_PATH, renderSectionPalettes(data), 'utf8');
  console.log(`Generated ${path.relative(ROOT, OUTPUT_PATH)}`);
}
